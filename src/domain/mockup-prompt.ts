// src/domain/mockup-prompt.ts
export function buildMockupPrompt(productDoc: string): string {
  return [
    '아래 제품 문서를 바탕으로, 이 제품의 주요 화면들을 한 캔버스에 펼쳐 보여주는 *목업*을 만든다.',
    '출력은 **자체 완결형 HTML 문서 하나**다. 규칙:',
    '1) 인라인 <style>만 사용. 외부 리소스·폰트·이미지·스크립트(JS) 없음. 정적 HTML/CSS만.',
    '2) 제품 문서에 등장하는 기능/페이지마다 하나의 "아트보드"(카드)를 만들고, 가상의 임의 데이터로 그 화면을 그린다.',
    '3) 아트보드들을 옅은 회색 캔버스 위에 가로로 나열하고, 각 아트보드 위에 화면 이름 라벨을 단다.',
    '4) 깔끔한 그레이스케일, 둥근 카드, 실제 앱처럼 보이게.',
    '',
    '제품 문서:',
    '"""',
    productDoc,
    '"""',
    '',
    '설명 문장이나 코드펜스(```) 없이 완전한 HTML 문서만 출력하라.',
  ].join('\n');
}
